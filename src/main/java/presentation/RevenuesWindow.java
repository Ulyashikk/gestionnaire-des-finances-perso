    public Depense getNewRevenueInput() {
        JTextField descriptionField = new JTextField();
        JTextField amountField = new JTextField();
        JFormattedTextField dateField = createDateField();

        JPanel panel = new JPanel(new GridLayout(0, 1));
        panel.add(new JLabel("Description:"));
        panel.add(descriptionField);
        panel.add(new JLabel("Montant:"));
        panel.add(amountField);
        panel.add(new JLabel("Date (yyyy-MM-dd):"));
        panel.add(dateField);

        int result = JOptionPane.showConfirmDialog(null, panel, "Votre nouvelle revenue", JOptionPane.OK_CANCEL_OPTION);
        if (result == JOptionPane.OK_OPTION) {
            String description = descriptionField.getText();
            double amount = Double.parseDouble(amountField.getText());
            Date date = (Date) dateField.getValue();
            return new Depense(description, date, amount);
        } else {
            return null;
        }
    }
    
    public Depense modifyRevenueInput(Depense existingDepense) {
        JTextField descriptionField = new JTextField(existingDepense.getDescription());
        JTextField amountField = new JTextField(String.valueOf(existingDepense.getAmount()));
        JFormattedTextField dateField = createDateField();
        dateField.setValue(existingDepense.getDate());

        JPanel panel = new JPanel(new GridLayout(0, 1));
        panel.add(new JLabel("Description:"));
        panel.add(descriptionField);
        panel.add(new JLabel("Montant:"));
        panel.add(amountField);
        panel.add(new JLabel("Date (yyyy-mm-dd):"));
        panel.add(dateField);

        int result = JOptionPane.showConfirmDialog(null, panel, "Modifier Revenue", JOptionPane.OK_CANCEL_OPTION);
        if (result == JOptionPane.OK_OPTION) {
            String description = descriptionField.getText();
            double amount = Double.parseDouble(amountField.getText());
            Date date = (Date) dateField.getValue();
            return new Depense(description, date, amount);
        } else {
            return null;
        }
    }


    private JFormattedTextField createDateField() {
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        JFormattedTextField dateField = new JFormattedTextField(dateFormat);
        dateField.setColumns(10);
        dateField.setValue(new Date());
        return dateField;
    }
